num1 = int(input("Enter the first number:"))  # Accepts first number from user and convert to integer
num2 = int(input("Enter the second number:"))  # Accepts second number from user and convert to integer
print("Printing the results for all arithmetic operations:")  # Display message before showing results
print("Addition:", num1 + num2)  # Prints the sum of num1 and num2
print("Subtraction:", num1 - num2)  # Prints the difference of num1 and num2
print("Multiplication:", num1 * num2)  # Prints the product of num1 and num2
print("Division:", num1 / num2)  # Prints the quotient of num1 divided by num2
print("Modulus:", num1 % num2)  # Prints the remainder of num1 divided by num2
