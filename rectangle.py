length = float(input("Enter length of the rectangle"))  # Accepts length from user
breadth = float(input("Enter breadth of the rectangle"))  # Accepts breadth from user
area = length * breadth  # Calculates area of rectangle
perimeter = 2 * (length + breadth)  # Calculates perimeter of rectangle
print("Area of rectangle is =", area)  # Prins area of rectangle
print("Perimeter of rectangle is =", perimeter)  # Prints perimeter of rectangle